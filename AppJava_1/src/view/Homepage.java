    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String Fullname = jTextField1.getText();
        String Email = jTextField2.getText();
        int Age = Integer.parseInt(jTextField3.getText());

        if (Fullname.isEmpty() || Email.isEmpty() || jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "khong duoc de trong dong nao ");
        } else {
            User u = new User();
            u.setFullName(Fullname);
            u.setEmail(Email);
            u.setAge(Age);
            u.setID(e.getID());
            Boolean ok = new UserDAO().CheckEdit(u);
            if (ok) {
                JOptionPane.showMessageDialog(this, "thay doi thanh cong");
                update();
                e.setFullName(Fullname);
                e.setEmail(Email);
                e.setAge(Age);
            } else {
                JOptionPane.showMessageDialog(this, "thay doi that bai");
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String OldPassword = jPasswordField1.getText();
        String passWord = jPasswordField2.getText();
        String cfpassWord = jPasswordField3.getText();
        if (OldPassword.isEmpty() || passWord.isEmpty() || cfpassWord.isEmpty()) {
            JOptionPane.showMessageDialog(this, "khong duoc de trong dong nao ");
        } else if (passWord.compareTo(cfpassWord) != 0) {
            JOptionPane.showMessageDialog(this, "mat khau phai trung nhau");
        } else if (OldPassword.compareTo(e.getPassWord()) != 0) {
            JOptionPane.showMessageDialog(this, "mat khau cu sai");
        } else {
            User u = new User();
            u.setID(e.getID());
            u.setPassWord(passWord);
            Boolean ok = new UserDAO().CheckChangePass(u);
            if (ok) {
                JOptionPane.showMessageDialog(this, "thay doi thanh cong");
                e.setPassWord(passWord);
            } else {
                JOptionPane.showMessageDialog(this, "thay doi that bai");
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

