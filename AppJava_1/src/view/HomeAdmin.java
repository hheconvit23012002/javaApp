        String Exactly = jTextField6.getText();
        String ThemeQ = jLabel43.getText();
        int id=0;
        for(int i=0;i<listThemeQuestions.size();i++){
            if(ThemeQ.compareTo(listThemeQuestions.get(i).getNameThemeQuestion())==0){
                id=listThemeQuestions.get(i).getID();
            }
        }
        if(qs.isEmpty() || A.isEmpty() || B.isEmpty() || C.isEmpty()|| D.isEmpty() || Exactly.isEmpty()){
            JOptionPane.showMessageDialog(this, "khong duoc de trong");
        }
        else{
            Question q = new Question();
            q.setQuestionString(qs);
            q.setAnswerA(A);
            q.setAnswerB(B);
            q.setAnswerC(C);
            q.setAnswerD(D);
            q.setAnswerExactlyString(Exactly);
            q.setIdThemeQuestion(id);
            boolean ok= new UserDAO().checkAddQuestion(q);
            if(ok){
                JOptionPane.showMessageDialog(this, "them cau hoi thanh cong");
            }
            else JOptionPane.showMessageDialog(this, "them cau hoi that bai");
        }
