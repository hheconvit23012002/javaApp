    private void jPanel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel15MouseClicked
        // TODO add your handling code here:
        String English = jTextField1.getText();
        String Vietnamese = jTextField2.getText();
        String Nametheme = jLabel21.getText();
        int idthe = 0;
        for (int i = 0; i < listThemevo.size(); i++) {
            if (Nametheme.compareTo(listThemevo.get(i).getNameThemeVocabulary()) == 0) {
                idthe = listThemevo.get(i).getID();
            }
        }
        if (English.isEmpty() || Vietnamese.isEmpty()) {
            JOptionPane.showMessageDialog(this, "khong duoc bo trong");
        } else {
            Vocabulary vo = new Vocabulary();
            vo.setEnglish(English);
            vo.setVietnameseString(Vietnamese);
            vo.setIdtheme(idthe);
            Boolean ok = new UserDAO().checkAddVocabulary(vo);
            if (ok) {
                JOptionPane.showMessageDialog(this, "them tu vung thanh cong");
            } else {
                JOptionPane.showMessageDialog(this, "them tu that bai");
            }
        }
    }//GEN-LAST:event_jPanel15MouseClicked

